function showcat(cat)
{
  if(cat%100==0)
    document.write("不拘");
  else
  {
    for(i=0;i<catarray.length;i++)
      for(j=0;j<catarray[i].length;j++)
        if(catarray[i][j].substring(0,3) == cat)
          document.write(catarray[i][j].substring(3,30));
  }
}

function tocat(num,cat,case_cat,p_g)
{
  case_cat.selectedIndex=0;
  for(ctr=0;ctr<catarray[num].length;ctr++)
  {
    if(ctr==0)
    {
      if(cat==0 || cat=="")
      {
        case_cat.options[0] = new Option("====== 請選擇小類 ======","");
        case_cat.options[0].selected = true;
      }
      else
      {
        if(cat.substring(1,3)=="00")
        {
          optionstr=num+"00";
          case_cat.options[0] = new Option("不拘",optionstr);
          case_cat.options[0].selected = true;
        }
        else
        {
          for(i=0;i<catarray.length;i++)
          {
            for(j=0;j<catarray[i].length;j++)
            {
              if(catarray[i][j].substring(0,3) == cat)
              {
                case_cat.options[0] = new Option(catarray[i][j].substring(3,30),cat);
                case_cat.options[0].selected = true;
              }
            }
          }
        }
      }
    }
    else
    {
      case_cat.options[ctr] = new Option(catarray[num][ctr].substring(3,30),catarray[num][ctr].substring(0,3));
    }
  }
  case_cat.length=catarray[num].length;
  if(p_g=="g" && (!(num==0)))
  {
    optionstr=catarray[num][1].substring(0,1)+"00";
    optionlen=catarray[num].length;
    case_cat.options[optionlen] = new Option("不拘",optionstr);
  }
}