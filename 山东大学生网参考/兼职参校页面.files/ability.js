function toability(num,v,Ability) 
{ 
  Ability.selectedIndex=0;
  for(ctr=0;ctr<pc_lan_ability[num].length;ctr++)
  {
    if(ctr==0)
    {
      Ability.options[0] = new Option("====請選擇專長小類====","");
      Ability.options[0].selected = true;
    }
    else
    {
      m=pc_lan_ability[num][ctr].indexOf(",");
      Ability.options[ctr] = new Option(pc_lan_ability[num][ctr].substring(m+1,30),pc_lan_ability[num][ctr].substring(0,m));
      for(i=0;i<pc_lan_ability[num].length;i++)
      {
        m=pc_lan_ability[num][i].indexOf(",");
        if(pc_lan_ability[num][i].substring(0,m) == v)
        {
          Ability.options[i].selected = true;
          break;
        }
      }
    }
  }
  Ability.length=pc_lan_ability[num].length;
}